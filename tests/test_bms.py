# Version: "BMS V1.0"
# INFO = hex-encoded ASCII "BMS V1.0" = 424D532056312E30 (16 chars, LENID=010)
VERSION_RESPONSE = b"~25014600F010424D532056312E30FA46\r"

# Serial: BMS SN = "BMS001234567", Pack SN = "PCK9876543"
# INFO[0:30] = bms_sn as hex, INFO[30:40] = gap, INFO[40:68] = pack_sn as hex
SERIAL_RESPONSE = b"~250146008044424D53303031323334353637202020000000000050434B3938373635343320202020F012\r"

# Analog: 1 pack, 2 cells (3300, 3310 mV), 2 temps (25.0, 20.0 °C),
#   i_pack=10.0 A, v_pack=51.2 V, i_remain=10000 mAh, i_full=20000 mAh,
#   cycles=42, i_design=20000 mAh → SOC=50.0%, SOH=100.0%
# INFO: DATAFLG(00) + packs(01) + cells(02) + cell1(0CE4) + cell2(0CEE)
#      + temps(02) + temp1(0BA4) + temp2(0B72) + i_pack(03E8) + v_pack(C800)
#      + i_remain(03E8) + P(03) + i_full(07D0) + cycles(002A) + i_design(07D0) + reserved(00)
ANALOG_RESPONSE = b"~2501460090340001020CE40CEE020BA40B7203E8C80003E80307D0002A07D000F287\r"

# Pack capacity: remain=10000, full=20000, design=20000 mAh
CAPACITY_RESPONSE = b"~25014600400C03E807D007D0FB01\r"

# Warn: 1 pack, 2 cells (no cell/temp warnings), instruction=0x06 → charge_fet=1, discharge_fet=1
# INFO: DATAFLG(00) + pack_count_echo(01) + cells(02) + cell1(00) + cell2(00)
#      + temps(02) + temp1(00) + temp2(00) + charge_cur(00) + total_volt(00) + disch_cur(00)
#      + protect1(00) + protect2(00) + instruction(06) + control(00) + fault(00)
#      + balance1(00) + balance2(00) + warn1(00) + warn2(00)
WARN_RESPONSE = b"~2501460060280001020000020000000000000006000000000000F613\r"


def _mock_transport(response: bytes) -> MagicMock:
    """Return a mock transport that returns *response* from receive()."""
    transport = MagicMock()
    transport.receive.return_value = response
    return transport


def _mock_transport_sequence(*responses: bytes) -> MagicMock:
    """Return a mock transport that cycles through *responses* in order."""
    transport = MagicMock()
    transport.receive.side_effect = list(responses)
    return transport


