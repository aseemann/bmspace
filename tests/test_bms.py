class TestGetWarnInfo:
    def test_returns_list_with_one_entry(self):
        transport = _mock_transport(WARN_RESPONSE)
        warn_list = get_warn_info(transport, packs=1)
        assert len(warn_list) == 1

    def test_returns_pack_warn_info_instances(self):
        transport = _mock_transport(WARN_RESPONSE)
        warn_list = get_warn_info(transport, packs=1)
        assert all(isinstance(w, PackWarnInfo) for w in warn_list)

    def test_pack_number(self):
        transport = _mock_transport(WARN_RESPONSE)
        warn = get_warn_info(transport, packs=1)[0]
        assert warn.pack_number == 1

    def test_no_warnings_when_all_normal(self):
        transport = _mock_transport(WARN_RESPONSE)
        warn = get_warn_info(transport, packs=1)[0]
        assert warn.warnings == ""

    def test_charge_fet_on(self):
        # Instruction byte = 0x06 = 0b00000110 → bit1=charge_fet, bit2=discharge_fet
        transport = _mock_transport(WARN_RESPONSE)
        warn = get_warn_info(transport, packs=1)[0]
        assert warn.charge_fet == 1

    def test_discharge_fet_on(self):
        transport = _mock_transport(WARN_RESPONSE)
        warn = get_warn_info(transport, packs=1)[0]
        assert warn.discharge_fet == 1

    def test_current_limit_off(self):
        # Instruction byte = 0x06, bit0 = current_limit = 0
        transport = _mock_transport(WARN_RESPONSE)
        warn = get_warn_info(transport, packs=1)[0]
        assert warn.current_limit == 0

    def test_no_protection_flags_set(self):
        transport = _mock_transport(WARN_RESPONSE)
        warn = get_warn_info(transport, packs=1)[0]
        assert warn.prot_short_circuit == 0
        assert warn.prot_discharge_current == 0
        assert warn.prot_charge_current == 0

    def test_balancing_all_zeros(self):
        transport = _mock_transport(WARN_RESPONSE)
        warn = get_warn_info(transport, packs=1)[0]
        assert warn.balancing1 == "00000000"
        assert warn.balancing2 == "00000000"

    def test_raises_on_protocol_error(self):
        transport = _mock_transport(b"GARBAGE\r")
        with pytest.raises(RuntimeError):
            get_warn_info(transport, packs=1)
